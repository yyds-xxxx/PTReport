class I18n:
    def __init__(self, lang='zh'):
        self.lang = lang
        self.strings = LANG_ZH if lang == 'zh' else LANG_EN

    def get(self, key, **kwargs):
        text = self.strings.get(key, key)
        if kwargs:
            text = text.format(**kwargs)
        return text

    def t(self, key):
        return self.get(key)

    def set_lang(self, lang):
        self.lang = lang
        self.strings = LANG_ZH if lang == 'zh' else LANG_EN